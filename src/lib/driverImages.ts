// Helper to get image by driver ID (fuzzy match or direct)
export function getDriverImage(driverId: string): string {
    // Try exact match
    if (DRIVER_IMAGES[driverId]) return DRIVER_IMAGES[driverId];

    // Try finding by last name
    const key = Object.keys(DRIVER_IMAGES).find(k => driverId.includes(k));
    if (key) return DRIVER_IMAGES[key];

    // Use a reliable fallback image (generic avatar)
    return "https://www.formula1.com/etc/designs/fom-website/images/f1_default_driver.png";
}

export function getTeamColor(constructorName: string): string {
    // Try exact match
    if (TEAM_COLORS[constructorName]) return TEAM_COLORS[constructorName];

    // Try partial match
    const key = Object.keys(TEAM_COLORS).find(k => constructorName.includes(k) || k.includes(constructorName));
    if (key) return TEAM_COLORS[key];

    return "#808080"; // Default gray
}
